	function get_rim_car($rim, $name) {
		for($i = 0; $i < count($rim); $i++) {
			if ($rim[$i]['name'] == $name) {
				return json_encode($rim[$i]['cars']);
			}
		}
	}

	function get_full_year($yr_range) {
		$old_yr_arr = explode('-', $yr_range);
		$new_yr_arr = array();
		while($old_yr_arr[0] <= $old_yr_arr[1]) {
			$new_yr_arr[] = $old_yr_arr[0]++;
		}

		$full_yr = implode(', ', $new_yr_arr);
		return $full_yr;
	}
